package Learning;

public class Switch {
	public static void main(String[] args) {
		String str="parth";
		switch(str){
		case "two":
			System.out.println("number is 0");
			break;
		case"parth":
			System.out.println("number is 1");
			break;
			default:
				System.out.println(str);
		}
		
		
	}

}
